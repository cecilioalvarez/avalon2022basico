package introduccion;

public class Ejemplo006Bucle {

    public static void main(String[] args) {
        int inicio=0;
        int fin=4;
        // bucle
        //repeticion
        while( inicio<fin) {
            System.out.println("hola");
            // incrementa el valor de la variable de uno en uno
            // de tal que en algun momento pasara a valer 4
            // cuando valga 4 el bucle termina
            inicio=inicio+1;

        }




    }
}
