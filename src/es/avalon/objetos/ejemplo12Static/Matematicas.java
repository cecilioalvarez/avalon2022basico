package es.avalon.objetos.ejemplo12Static;

public class Matematicas {


    //Metodos de clase o estaticas, no es necesario crear objetos para utilizarlas.
    public static double sumar(double n1, double n2){
        return n1+n2;
    }

    public static double restar(double n1, double n2){
        return n1-n2;
    }

    public static double producto(double n1, double n2){
        return n1*n2;
    }

    public static double division(double n1, double n2){
        return n1/n2;
    }
}
