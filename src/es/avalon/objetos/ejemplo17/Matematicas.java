package es.avalon.objetos.ejemplo17;

public class Matematicas {
    public double sumar (int n1, int n2) {

        return n1+n2;
    }

    public double restar (int n1, int n2) {

        return n1-n2;
    }

    public double producto (int n1, int n2) {

        return n1*n2;
    }

    public double division (int n1, int n2) {

        return n1/n2;
    }
}
