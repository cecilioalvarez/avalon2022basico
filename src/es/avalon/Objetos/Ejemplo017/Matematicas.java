package es.avalon.Objetos.Ejemplo017;

public class Matematicas {

    public static double sumar (int n1, int n2) {

        return n1+n2;
    }

    public static double restar (int n1, int n2) {

        return n1-n2;
    }

    public static double producto (int n1, int n2) {

        return n1*n2;
    }

    public static double division (int n1, int n2) {

        return n1/n2;
    }
}
